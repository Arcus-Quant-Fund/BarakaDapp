def fig2_scatter(panel):
    fig, ax = plt.subplots(figsize=(6.5, 5.5))
    colors = plt.cm.tab10(np.linspace(0, 1, 7))
    for i, country in enumerate(COUNTRY_ORDER):
        sub = panel[(panel["country"] == country)
                    & panel["cds_spread_bps"].notna()
                    & panel["s_conv_bps"].notna()]
        ax.scatter(sub["cds_spread_bps"], sub["s_conv_bps"],
                   s=15, alpha=0.55, color=colors[i], label=country)

    # 45-degree line
    lim = max(panel["cds_spread_bps"].max(), panel["s_conv_bps"].max()) * 1.05
    ax.plot([0, lim], [0, lim], "--", color="grey", lw=1, label="45° line")

    # OLS fit
    mask = panel["cds_spread_bps"].notna() & panel["s_conv_bps"].notna()
    x = panel.loc[mask, "cds_spread_bps"].values
    y = panel.loc[mask, "s_conv_bps"].values
    m, b = np.polyfit(x, y, 1)
    xr = np.linspace(0, lim, 200)
    ax.plot(xr, m*xr+b, "-", color="black", lw=1.2,
            label=f"OLS: slope={m:.3f}, intercept={b:.1f}")

    ss_res = np.sum((y - (m*x+b))**2)
    ss_tot = np.sum((y - y.mean())**2)
    r2 = 1 - ss_res/ss_tot

    ax.set_xlabel("Damodaran CDS spread (bps)", fontsize=10)
    ax.set_ylabel(r"$s_\text{conv}(\iota=\text{SOFR})$ (bps)", fontsize=10)
    ax.set_title(f"AHJ Formula vs.\ Damodaran CDS  ($R^2={r2:.3f}$, $N={mask.sum():,}$)",
                 fontsize=10)
    ax.legend(fontsize=7.5, ncol=2)
    ax.set_xlim(0, lim); ax.set_ylim(0, lim)

    path = FIGS / "fig2_scatter.pdf"
    plt.tight_layout()
    plt.savefig(path, dpi=160, bbox_inches="tight")
    plt.close()
    print(f"  {path.name}")


