def print_summary(panel, cir_tbl, gas_tbl):
    print("\n── Key numbers for paper body ──────────────────────────────")
    mask = panel["cds_spread_bps"].notna() & panel["s_conv_bps"].notna()
    x  = panel.loc[mask, "cds_spread_bps"].values
    y  = panel.loc[mask, "s_conv_bps"].values
    m, b = np.polyfit(x, y, 1)
    ss_res = np.sum((y-(m*x+b))**2)
    r2 = 1 - ss_res/np.sum((y-y.mean())**2)
    rmse = np.sqrt(np.mean((y-x)**2))
    print(f"  Scatter R² (s_conv vs CDS): {r2:.4f}")
    print(f"  RMSE s_conv vs CDS: {rmse:.2f} bps")
    print(f"  OLS slope: {m:.4f}, intercept: {b:.2f}")
    print(f"  N obs in scatter: {mask.sum():,}")

    mean_riba = panel["riba_premium_bps"].mean()
    print(f"  Mean riba premium (all): {mean_riba:.2f} bps")
    inv_grade = ["UAE","Kuwait","Qatar","Saudi Arabia","Malaysia"]
    inv_panel = panel[panel["country"].isin(inv_grade)]
    print(f"  Mean riba premium (inv. grade): {inv_panel['riba_premium_bps'].mean():.2f} bps")

    full_lc = gas_tbl[gas_tbl["step"].str.startswith("Full")].iloc[0]
    print(f"  Full lifecycle gas: {full_lc.gas_units:,} units  ~${full_lc.usd_cost:.4f}")

    print(f"  Mean %above CDS (long-run): {cir_tbl['pct_above_cds'].mean():.1f}%")

    # Error between s_conv and Damodaran for inv-grade GCC
    gcc_inv = ["UAE","Kuwait","Qatar","Saudi Arabia"]
    sub = cir_tbl[cir_tbl["country"].isin(gcc_inv)]
    errs = (sub["s_conv_lr"] - sub["cds_2025"]) / sub["cds_2025"] * 100
    print(f"  GCC inv-grade s_conv vs CDS 2025 errors: {errs.values}")
    print(f"    Mean abs error: {errs.abs().mean():.1f}%")


