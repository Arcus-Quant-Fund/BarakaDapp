def fig5_welfare(welfare):
    W0 = 10_000_000.0
    kappa_sa = 0.02409; T = 5
    p_def = 1 - np.exp(-kappa_sa * T)

    fig, axes = plt.subplots(1, 3, figsize=(12, 4.5), sharey=False)
    gammas = [2, 5, 10]
    strategy_labels = ["Unhedged", "iCDS ($\\iota=0$)", "Conv.\ CDS"]

    for j, gamma in enumerate(gammas):
        ax = axes[j]
        r  = welfare[welfare["gamma"] == gamma].iloc[0]

        W_nh_ok  = W0
        W_nh_def = W0 * (1 - LGD)
        W_icds   = W0 - r.prem_icds_usd
        W_cds    = W0 - r.prem_cds_usd

        def eu(w_ok, w_def, g):
            def u(w):
                if g == 1: return np.log(max(w,1))
                return (max(w,1)**(1-g))/(1-g)
            return (1-p_def)*u(w_ok) + p_def*u(w_def)

        def cew(eu_v, g):
            if g == 1: return np.exp(eu_v)
            return (eu_v * (1-g)) ** (1/(1-g))

        eu_nh   = eu(W_nh_ok, W_nh_def, gamma)
        eu_icds = eu(W_icds,  W_icds,   gamma)
        eu_cds  = eu(W_cds,   W_cds,    gamma)

        cews = [cew(eu_nh, gamma)/1e6, cew(eu_icds, gamma)/1e6, cew(eu_cds, gamma)/1e6]
        colors = ["tomato", "steelblue", "gold"]
        bars = ax.bar(strategy_labels, cews, color=colors, alpha=0.85, edgecolor="black", lw=0.5)

        for bar, v in zip(bars, cews):
            ax.text(bar.get_x() + bar.get_width()/2, v + 0.01,
                    f"${v:.2f}M", ha="center", va="bottom", fontsize=8)

        ax.set_title(f"$\\gamma = {gamma}$", fontsize=10)
        ax.set_ylabel("Certainty-equivalent wealth ($M)" if j == 0 else "")
        ax.tick_params(axis="x", labelsize=8)
        ax.set_ylim(min(cews)*0.97, max(cews)*1.03)

    fig.suptitle("Certainty-Equivalent Wealth by Hedging Strategy\n"
                 "(Saudi Arabia sovereign, $W_0=\\$10$M, 5-year tenor)", fontsize=10.5)
    plt.tight_layout()
    path = FIGS / "fig5_welfare.pdf"
    plt.savefig(path, dpi=160, bbox_inches="tight")
    plt.close()
    print(f"  {path.name}")


