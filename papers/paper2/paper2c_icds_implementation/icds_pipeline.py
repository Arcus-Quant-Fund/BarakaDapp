def table5_welfare(welfare):
    # Compute certainty-equivalent wealth (CEW)
    W0 = 10_000_000.0

    def cew(eu, gamma):
        """Inverse CRRA: CEW = (EU × (1-γ))^(1/(1-γ))"""
        if gamma == 1:
            return np.exp(eu)
        return (eu * (1 - gamma)) ** (1 / (1 - gamma))

    lines = [
        r"% Table 5: Welfare Analysis",
        r"% Auto-generated by icds_pipeline.py",
        r"\begin{table}[htbp]",
        r"\centering",
        r"\caption{Welfare Analysis: Certainty-Equivalent Wealth for Saudi Arabia 5yr Sukuk.}",
        r"\label{tab:welfare}",
        r"\small",
        r"\begin{tabular}{lrrrrr}",
        r"\toprule",
        r"$\gamma$ & Premium (iCDS) & Premium (CDS) & CEW: Unhedged & CEW: iCDS & CEW: Conv.\ CDS \\",
        r"\midrule",
    ]
    for _, r in welfare.iterrows():
        gamma = int(r.gamma)
        # Compute expected utilities for CEW back-conversion
        # Unhedged
        kappa_sa = 0.02409; T = 5
        p_def = 1 - np.exp(-kappa_sa * T)
        W_nh_ok  = W0
        W_nh_def = W0 * (1 - LGD)
        W_icds   = W0 - r.prem_icds_usd
        W_cds    = W0 - r.prem_cds_usd

        def eu(w_ok, w_def, g):
            def u(w):
                if g == 1: return np.log(max(w,1))
                return (max(w,1)**(1-g))/(1-g)
            return (1-p_def)*u(w_ok) + p_def*u(w_def)

        eu_nh   = eu(W_nh_ok, W_nh_def, gamma)
        eu_icds = eu(W_icds,  W_icds,   gamma)
        eu_cds  = eu(W_cds,   W_cds,    gamma)

        cew_nh   = cew(eu_nh,   gamma)
        cew_icds = cew(eu_icds, gamma)
        cew_cds  = cew(eu_cds,  gamma)

        lines.append(
            f"{gamma} & \\${r.prem_icds_usd:,.0f} & \\${r.prem_cds_usd:,.0f} & "
            f"\\${cew_nh:,.0f} & \\${cew_icds:,.0f} & \\${cew_cds:,.0f} \\\\"
        )
    lines += [
        r"\bottomrule",
        r"\end{tabular}",
        r"\begin{minipage}{\textwidth}",
        r"\smallskip\footnotesize",
        r"\textit{Notes}: CRRA utility $u(W)=W^{1-\gamma}/(1-\gamma)$."
        r" Certainty-equivalent wealth (CEW) is the riskless wealth that yields the same"
        r" expected utility as the risky strategy."
        r" Reference entity: Saudi Arabia sovereign; notional $W_0=\$10{,}000{,}000$;"
        r" $T=5$ years; $\kappa=\hat{\bar{\kappa}}_\text{SA}=2.409\%$; $\delta=0.60$;"
        r" $\iota_\text{CDS}=\text{SOFR}_{2025}=5.34\%$."
        r" iCDS premium $=\hat{\kappa}(1{-}\delta)\cdot W_0\cdot T$;"
        r" CDS premium uses AHJ formula at $\iota=\text{SOFR}$.",
        r"\end{minipage}",
        r"\end{table}", "",
    ]
    (TABS / "table5_welfare.tex").write_text("\n".join(lines))
    print("  table5_welfare.tex")


