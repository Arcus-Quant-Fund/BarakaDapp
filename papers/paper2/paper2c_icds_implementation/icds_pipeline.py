def table2_pricing_comparison(panel, sofr):
    # Annual cross-country means
    panel["year"] = pd.to_datetime(panel["obs_date"]).dt.year
    ann = (panel.groupby(["country","year"])
                .agg(s_star=("s_star_bps","mean"),
                     s_conv=("s_conv_bps","mean"),
                     s_cds =("cds_spread_bps","mean"))
                .reset_index())
    # Pivot to wide for selected years
    years = [2015, 2018, 2020, 2022, 2023, 2025]
    order = ["UAE","Kuwait","Qatar","Saudi Arabia","Malaysia","Indonesia","Bahrain"]
    lines = [
        r"% Table 2: Annual Pricing Comparison",
        r"% Auto-generated by icds_pipeline.py",
        r"\begin{table}[htbp]",
        r"\centering",
        r"\caption{Annual iCDS vs.\ Conventional CDS Spreads (basis points).}",
        r"\label{tab:pricing}",
        r"\small",
        r"\setlength{\tabcolsep}{4pt}",
        r"\begin{tabular}{l" + "rrr" * len(years) + "}",
        r"\toprule",
        r"\multicolumn{1}{l}{} & " +
        " & ".join([r"\multicolumn{3}{c}{" + str(y) + "}" for y in years]) + r" \\",
        r"\cmidrule(lr){2-4}" + "".join([
            r"\cmidrule(lr){" + str(3*i+2) + "-" + str(3*i+4) + "}"
            for i in range(1, len(years))
        ]),
        "Country & " + " & ".join([r"$s^*$ & $s_\text{c}$ & $s_\text{obs}$"] * len(years)) + r" \\",
        r"\midrule",
    ]
    for country in order:
        row_parts = [country]
        sub = ann[ann["country"] == country]
        for y in years:
            yr = sub[sub["year"] == y]
            if len(yr):
                r = yr.iloc[0]
                s_star = f"{r.s_star:.0f}" if not np.isnan(r.s_star) else "—"
                s_conv = f"{r.s_conv:.0f}" if not np.isnan(r.s_conv) else "—"
                s_cds  = f"{r.s_cds:.0f}"  if not np.isnan(r.s_cds) else "—"
            else:
                s_star = s_conv = s_cds = "—"
            row_parts += [s_star, s_conv, s_cds]
        lines.append(" & ".join(row_parts) + r" \\")
    lines += [
        r"\bottomrule",
        r"\end{tabular}",
        r"\begin{minipage}{\textwidth}",
        r"\smallskip\footnotesize",
        r"\textit{Notes}: $s^*=\hat{\kappa}(1{-}\delta)\times10{,}000$ (iCDS at $\iota=0$)."
        r" $s_\text{c}=\hat{\kappa}(1{-}\delta)\cdot\iota/(\hat{\kappa}+\iota)\times10{,}000$"
        r" (AHJ formula at $\iota=$SOFR). $s_\text{obs}$ = Damodaran (2026) sovereign"
        r" CDS spread. All in basis points. $\delta=0.60$.",
        r"\end{minipage}",
        r"\end{table}", "",
    ]
