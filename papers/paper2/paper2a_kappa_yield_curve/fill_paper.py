def export_table5_panel(models, df, path):
    """Extract coefficients and format panel regression table."""
    if not models:
        print("[panel_reg] No models — statsmodels missing. Generating synthetic results.")
        # Generate plausible synthetic table (demo data, κ by construction = spread/(1-δ)*10000)
        with open(path, "w") as f:
            f.write("% Table 5: Panel Regression (synthetic demo — awaiting Bloomberg data)\n")
            f.write("% Auto-generated by fill_paper.py\n")
            f.write("% NOTE: Exact results require real data; see horse_race.csv\n")
        return {}

    n_obs = len(df.dropna(subset=["kappa_hat", "spread_bps"]))
    specs = list(models.keys())

    with open(path, "w") as f:
        f.write("% Table 5: Panel Regression Results\n")
        f.write("% Auto-generated by fill_paper.py\n")
        f.write("\\begin{table}[t]\n\\centering\n")
        f.write("\\caption{Panel Regression: Sukuk Spread on $\\hat{\\kappa}$}\n")
        f.write("\\label{tab:panel_auto}\n")
        f.write("\\small\n")
        f.write("\\resizebox{\\textwidth}{!}{%\n")

        ncol = len(specs)
        col_fmt = "l" + "c" * ncol
        f.write(f"\\begin{{tabular}}{{{col_fmt}}}\n\\toprule\n")

        # Header
        header = " & " + " & ".join([f"({i+1})" for i in range(ncol)]) + " \\\\\n"
        f.write(header)
        names_row = " & " + " & ".join([s.split(")")[0].strip("(").strip() for s in specs]) + " \\\\\n"
        f.write(names_row)
        f.write("\\midrule\n")

        # β(κ̂) row
        betas = []
        ses = []
        pvals_h1 = []  # p-value for H0: β=1
        r2s = []
        for name, m in models.items():
            beta = m.params.get("kappa_hat", np.nan)
            se   = m.bse.get("kappa_hat", np.nan)
            betas.append(beta)
            ses.append(se)
            # t-stat for H0: β=1
            t_h1 = (beta - 1) / se if se > 0 else np.nan
            from scipy.stats import norm
            pval_h1 = 2 * (1 - norm.cdf(abs(t_h1))) if not np.isnan(t_h1) else np.nan
            pvals_h1.append(pval_h1)
            r2s.append(m.rsquared)

        def sig_stars(p):
            if np.isnan(p): return ""
            if p < 0.01: return "***"
            if p < 0.05: return "**"
            if p < 0.10: return "*"
            return ""

        beta_str = "$\\hat{\\kappa}$ ($\\hat{\\beta}$) & " + \
                   " & ".join([f"{b:.4f}{sig_stars(m.pvalues.get('kappa_hat',1))}"
                                for b, m in zip(betas, models.values())]) + " \\\\\n"
        se_str = " & " + " & ".join([f"({s:.4f})" for s in ses]) + " \\\\\n"
        pval_str = "$H_0$: $\\beta=1$, $p$ & " + \
                   " & ".join([(f"{p:.3f}" if not np.isnan(p) else "---") for p in pvals_h1]) + " \\\\[4pt]\n"

        f.write(beta_str)
        f.write(se_str)
        f.write(pval_str)

        # Full spec controls if available (spec 5)
        full_name = "(5) Full" if "(5) Full" in models else None
        for ctrl_var, ctrl_label in [("rating_code", "Rating"), ("log_maturity", "$\\log$(Maturity)")]:
            if full_name and ctrl_var in models[full_name].params:
                coef = models[full_name].params[ctrl_var]
                se_c = models[full_name].bse[ctrl_var]
                p_c  = models[full_name].pvalues[ctrl_var]
                f.write(f"{ctrl_label} & & & & & {coef:.4f}{sig_stars(p_c)} \\\\\n")
                f.write(f" & & & & & ({se_c:.4f}) \\\\\n")

        f.write("\\midrule\n")
        fe_rows = {
            "(1) OLS":       ("No",  "No"),
            "(2) Country FE":("Yes", "No"),
            "(3) Sector FE": ("No",  "Yes"),
            "(4) Two-way FE":("Yes", "Yes"),
        }
        cfe = [fe_rows.get(s, ("Yes", "Yes"))[0] for s in specs]
        sfe = [fe_rows.get(s, ("Yes", "Yes"))[1] for s in specs]
        f.write("Country FE & " + " & ".join(cfe) + " \\\\\n")
        f.write("Sector FE  & " + " & ".join(sfe) + " \\\\\n")
        f.write(f"$N$ obs & \\multicolumn{{{ncol}}}{{c}}{{{n_obs:,}}} \\\\\n")
        r2_str = "$R^2$ & " + " & ".join([f"{r:.3f}" for r in r2s]) + " \\\\\n"
        f.write(r2_str)

        f.write("\\bottomrule\n\\end{tabular}\n}\n")
        f.write("\\caption*{\\textit{Note:} Dependent variable: sukuk spread (bps). "
                "HC3 heteroskedasticity-robust standard errors in parentheses. "
                "*, **, *** indicate significance at 10\\%, 5\\%, 1\\% levels. "
                "$H_0$: $\\beta=1$ (ABIslam2026 Theorem~1).}\n")
        f.write("\\end{table}\n")

    print(f"[export] Saved {path}")
    return {"betas": betas, "ses": ses, "pvals_h1": pvals_h1, "r2s": r2s, "n_obs": n_obs}

