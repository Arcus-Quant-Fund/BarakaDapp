# TABLE 3: NSS parameters (mean per country over all dates)
# ──────────────────────────────────────────────────────────────────────────────
def export_table3_nss(nss_df, path):
    country_map = {"SA": "Saudi Arabia", "AE": "UAE", "QA": "Qatar",
                   "MY": "Malaysia", "ID": "Indonesia"}
    country_order = ["SA", "AE", "QA", "MY", "ID"]

    rows = []
    for code in country_order:
        c_df = nss_df[nss_df["country"] == code]
        if len(c_df) == 0:
            continue
        label = country_map.get(code, code)
        b0 = c_df["beta0"].mean()
        b1 = c_df["beta1"].mean()
        b2 = c_df["beta2"].mean()
        b3 = c_df["beta3"].mean()
        t1 = c_df["tau1"].mean()
        t2 = c_df["tau2"].mean()
        rmse = c_df["nss_rmse_bps"].mean()
        rows.append((label, b0, b1, b2, b3, t1, t2, rmse))

    # Pooled GCC (SA, AE, QA) and SEA (MY, ID)
    gcc = nss_df[nss_df["country"].isin(["SA", "AE", "QA"])]
    sea = nss_df[nss_df["country"].isin(["MY", "ID"])]
    for label, grp in [("Pooled GCC", gcc), ("Pooled SEA", sea)]:
        if len(grp) == 0:
            continue
        rows.append((label, grp["beta0"].mean(), grp["beta1"].mean(),
                     grp["beta2"].mean(), grp["beta3"].mean(),
                     grp["tau1"].mean(), grp["tau2"].mean(),
                     grp["nss_rmse_bps"].mean()))

    with open(path, "w") as f:
        f.write("% Table 3: NSS Parameter Estimates (mean over 2014-2025 panel)\n")
        f.write("% Auto-generated by fill_paper.py\n")
        f.write("\\begin{table}[t]\n\\centering\n")
        f.write("\\caption{Nelson--Siegel--Svensson Parameter Estimates (Period Average).}\n")
        f.write("\\label{tab:nss_params_auto}\n")
        f.write("\\small\n")
        f.write("\\resizebox{\\textwidth}{!}{%\n")
        f.write("\\begin{tabular}{lcccccc|c}\n\\toprule\n")
        f.write("\\textbf{Country/Group} & $\\hat{\\beta}_0$ & $\\hat{\\beta}_1$ "
                "& $\\hat{\\beta}_2$ & $\\hat{\\beta}_3$ & $\\hat{\\tau}_1$ "
                "& $\\hat{\\tau}_2$ & \\textbf{RMSE} \\\\\n")
        f.write("& \\textit{(level)} & \\textit{(slope)} & \\textit{(curv.~1)} "
                "& \\textit{(curv.~2)} & & & \\textit{(bps)} \\\\\n")
        f.write("\\midrule\n")
        for i, (lbl, b0, b1, b2, b3, t1, t2, rmse) in enumerate(rows):
            if lbl.startswith("Pooled") and i > 0:
                f.write("\\midrule\n")
            f.write(f"{lbl} & {b0*100:.3f}\\% & {b1*100:.3f}\\% "
                    f"& {b2*100:.3f}\\% & {b3*100:.3f}\\% "
                    f"& {t1:.2f} & {t2:.2f} & {rmse:.2f} \\\\\n")
        f.write("\\bottomrule\n\\end{tabular}\n}\n")
        f.write("\\caption*{\\textit{Note:} Parameters estimated by NLS over "
                "2014--2025 panel, averaged over quarterly observations. "
                "$\\hat{\\beta}_0$ = long-run $\\kappa$ level (\\%). "
                "RMSE in basis points.}\n")
        f.write("\\end{table}\n")
    print(f"[export] Saved {path}")
    return rows

nss_rows = export_table3_nss(nss_df, f"{TAB_DIR}/table3_nss_params.tex")

