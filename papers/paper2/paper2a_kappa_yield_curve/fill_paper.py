# TABLE 4: Horse race
# ──────────────────────────────────────────────────────────────────────────────
def export_table4_horse_race(hr, path):
    with open(path, "w") as f:
        f.write("% Table 4: Horse Race — κ-model vs SOFR-model vs Random Walk\n")
        f.write("% Auto-generated by fill_paper.py\n")
        f.write("\\begin{table}[t]\n\\centering\n")
        f.write("\\caption{Out-of-Sample Forecast Accuracy: $\\kappa$-Model vs.\\ Alternatives}\n")
        f.write("\\label{tab:horse_race_auto}\n")
        f.write("\\small\n")
        f.write("\\resizebox{\\textwidth}{!}{%\n")
        f.write("\\begin{tabular}{lcccccc}\n\\toprule\n")
        f.write("\\textbf{Model} & \\textbf{RMSE (bps)} & \\textbf{MAE (bps)} "
                "& $\\mathbf{R^2}$ & \\textbf{DM stat} & \\textbf{DM $p$-val} \\\\\n")
        f.write("\\midrule\n")
        for _, row in hr.iterrows():
            model = row["Model"]
            rmse = f"{row['RMSE_bps']:.2f}" if row['RMSE_bps'] > 0.01 else "$<$0.01"
            mae  = f"{row['MAE_bps']:.2f}" if row['MAE_bps'] > 0.01 else "$<$0.01"
            r2   = f"{row['R2']:.3f}" if not np.isnan(row['R2']) else "---"
            dm   = f"{row['DM_vs_SOFR']:.2f}" if not np.isnan(row['DM_vs_SOFR']) else "---"
            pval = "0.000" if (not np.isnan(row['DM_pval']) and row['DM_pval'] < 0.001) \
                   else (f"{row['DM_pval']:.3f}" if not np.isnan(row['DM_pval']) else "---")
            f.write(f"$\\kappa$-model & {rmse} & {mae} & {r2} & {dm} & {pval} \\\\\n"
                    if model == "κ-model" else
                    f"{model} & {rmse} & {mae} & {r2} & {dm} & {pval} \\\\\n")
        f.write("\\bottomrule\n\\end{tabular}\n}\n")
        f.write("\\caption*{\\textit{Note:} $\\kappa$-model: predicted spread "
                "$= \\hat{\\kappa}(1-\\delta)$. SOFR-model: OLS regression of "
                "spread on SOFR, maturity, rating, country FE. DM test: $H_0$ "
                "equal predictive accuracy; negative stat $\\Rightarrow$ "
                "$\\kappa$-model is more accurate. $p < 0.001$ indicates rejection at "
                "all conventional levels.}\n")
        f.write("\\end{table}\n")
    print(f"[export] Saved {path}")

export_table4_horse_race(hr, f"{TAB_DIR}/table4_horse_race.tex")

